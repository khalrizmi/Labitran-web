	function Page_Main() {
		global $Language, $gbSkipHeaderFooter, $EW_EXPORT;

		// Check modal
		if ($this->IsModal)
			$gbSkipHeaderFooter = TRUE;

		// Load current record
		$bLoadCurrentRecord = FALSE;
		$sReturnUrl = "";
		$bMatchRecord = FALSE;
		if ($this->IsPageRequest()) { // Validate request
			if (@$_GET["c_jalur"] <> "") {
				$this->c_jalur->setQueryStringValue($_GET["c_jalur"]);
				$this->RecKey["c_jalur"] = $this->c_jalur->QueryStringValue;
			} elseif (@$_POST["c_jalur"] <> "") {
				$this->c_jalur->setFormValue($_POST["c_jalur"]);
				$this->RecKey["c_jalur"] = $this->c_jalur->FormValue;
			} else {
				$sReturnUrl = "jalurlist.php"; // Return to list
			}

			// Get action
			$this->CurrentAction = "I"; // Display form
			switch ($this->CurrentAction) {
				case "I": // Get a record to display
					if (!$this->LoadRow()) { // Load record based on key
						if ($this->getSuccessMessage() == "" && $this->getFailureMessage() == "")
							$this->setFailureMessage($Language->Phrase("NoRecord")); // Set no record message
						$sReturnUrl = "jalurlist.php"; // No matching record, return to list
					}
			}
		} else {
			$sReturnUrl = "jalurlist.php"; // Not page request, return to list
		}
		if ($sReturnUrl <> "")
			$this->Page_Terminate($sReturnUrl);

		// Set up Breadcrumb
		if ($this->Export == "")
			$this->SetupBreadcrumb();

		// Render row
		$this->RowType = EW_ROWTYPE_VIEW;
		$this->ResetAttrs();
		$this->RenderRow();

		// Set up detail parameters
		$this->SetupDetailParms();
	}

